# eval_score.py
# ─────────────────────────────────────────────────────────────────────────────
# Compute competition metrics (mAP, CMC) from a rankings CSV produced by
# eval_generate.py.
#
# Usage:
#   python eval_score.py --rankings outputs/rankings_rgb.csv \
#                        --labels   data/public_test_labels.csv
#
#   python eval_score.py --track all   # score all three tracks at once
# ─────────────────────────────────────────────────────────────────────────────

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Dict, List, Tuple

import pandas as pd


# ─────────────────────────────────────────────────────────────────────────────
# Metric helpers
# ─────────────────────────────────────────────────────────────────────────────

def _compute_ap(relevant_ranks: List[int], total_relevant: int) -> float:
    """Compute Average Precision for one query."""
    if total_relevant == 0:
        return 0.0
    hits = 0
    ap = 0.0
    for k, r in enumerate(sorted(relevant_ranks), start=1):
        hits += 1
        ap += hits / r
    return ap / total_relevant


def compute_metrics(
    rankings_csv: str,
    labels_csv: str,
    top_k: Tuple[int, ...] = (1, 5, 10),
) -> Dict[str, float]:
    """
    Compute mAP and CMC-k from a rankings CSV.

    Args:
        rankings_csv : Path to the CSV generated by eval_generate.py.
        labels_csv   : Path to the ground-truth labels CSV.
        top_k        : CMC ranks to report.

    Returns:
        Dictionary with keys: "mAP", "CMC-1", "CMC-5", "CMC-10", …
    """
    rankings = pd.read_csv(rankings_csv)
    labels = pd.read_csv(labels_csv)

    # Build ground-truth: gallery_id → person_id mapping
    gid_to_pid: Dict[str, int] = {
        str(gid): int(pid)
        for gid, pid in zip(labels["gallery_id"], labels["person_id"])
    }

    ap_scores: List[float] = []
    cmc_hits: Dict[int, int] = {k: 0 for k in top_k}
    num_queries = rankings["query_gallery_id"].nunique()

    for qid, group in rankings.groupby("query_gallery_id"):
        q_pid = gid_to_pid.get(str(qid))
        if q_pid is None:
            continue

        group_sorted = group.sort_values("rank")
        relevant_ranks: List[int] = []

        for _, row in group_sorted.iterrows():
            g_pid = gid_to_pid.get(str(row["gallery_id"]))
            if g_pid == q_pid:
                relevant_ranks.append(int(row["rank"]))

        # AP
        total_relevant = len(relevant_ranks)
        ap_scores.append(_compute_ap(relevant_ranks, total_relevant))

        # CMC
        first_hit = min(relevant_ranks) if relevant_ranks else float("inf")
        for k in top_k:
            if first_hit <= k:
                cmc_hits[k] += 1

    results: Dict[str, float] = {
        "mAP": sum(ap_scores) / len(ap_scores) if ap_scores else 0.0
    }
    for k in top_k:
        results[f"CMC-{k}"] = cmc_hits[k] / num_queries if num_queries else 0.0

    return results


# ─────────────────────────────────────────────────────────────────────────────
# Track map (matches eval_generate.py outputs)
# ─────────────────────────────────────────────────────────────────────────────

TRACK_OUTPUTS = {
    "rgb":   "outputs/rankings_rgb.csv",
    "depth": "outputs/rankings_depth.csv",
    "cross": "outputs/rankings_cross.csv",
}


# ─────────────────────────────────────────────────────────────────────────────
# CLI
# ─────────────────────────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Score Re-ID ranking CSVs for ICPR-2026-TVRID"
    )
    p.add_argument(
        "--track",
        choices=["rgb", "depth", "cross", "all"],
        default="all",
    )
    p.add_argument("--rankings", default=None,
                   help="Direct path to a rankings CSV (overrides --track)")
    p.add_argument("--labels",   default="data/public_test_labels.csv")
    p.add_argument("--top_k",    nargs="+", type=int, default=[1, 5, 10])
    return p.parse_args()


if __name__ == "__main__":
    args = parse_args()
    top_k = tuple(args.top_k)

    if args.rankings:
        # Score a single explicitly provided file
        metrics = compute_metrics(args.rankings, args.labels, top_k)
        print(f"\nResults for {args.rankings}")
        for k, v in metrics.items():
            print(f"  {k:<8}: {v:.4f}")
    else:
        tracks = (
            list(TRACK_OUTPUTS.keys()) if args.track == "all" else [args.track]
        )
        for track in tracks:
            csv_path = TRACK_OUTPUTS[track]
            if not Path(csv_path).exists():
                print(f"[{track.upper()}] Rankings file not found: {csv_path} — skipping")
                continue
            metrics = compute_metrics(csv_path, args.labels, top_k)
            print(f"\n[{track.upper()}] Results")
            for k, v in metrics.items():
                print(f"  {k:<8}: {v:.4f}")
